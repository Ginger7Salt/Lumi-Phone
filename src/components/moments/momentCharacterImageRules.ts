/** 角色朋友圈 / 私聊 / 群聊配图：模型侧描述规则（风格词由客户端拼接） */
export const CHARACTER_MEDIA_IMAGE_DESCRIPTION_RULES = `
# 配图描述视角（硬性）
- **非自拍（默认）**：角色用手机**第一视角**随手拍；镜头高度随站/坐姿势，**俯仰角与主体位置一致**。**不是**每张都要露出自己的脚、裤腿或手——只有构图需要时才写进画面边缘。
  - **平视风景/街景/窗外/海面/城市**：眼平高度横向取景，**画面以环境为主，可不出现任何肢体**；不要无人机/监控/第三人称。
  - **仰视**：拍天空、树梢、高楼、招牌、星空 → 向上举机，**通常不出现脚或裤腿**。
  - **俯视/地面级**：拍脚下猫狗、地面物件、台阶 → 向下拍；**此时**可在画面下缘写自己的鞋尖、裤脚、裙角等。
  - **桌台/手边**：咖啡、键盘、饭菜 → 略俯视；袖口或手指**可**入镜，非必须。
  - **比耶/手势**：第一视角下**自己的手或手指**从画面边缘入镜比耶、招手等，背景仍是所拍场景；不是第三人称拍全身。
  - 例（平视·无肢体）：雨后街景、霓虹倒映湿路面，平视，无人物肢体入镜。
  - 例（仰视·无肢体）：抬头拍傍晚天空与楼群轮廓，无脚或裤腿。
  - 例（俯视·有脚）：俯视，脚边橘猫蹲在地砖上，画面下方露出白色球鞋和牛仔裤裤脚。
  - 例（手势）：比耶，手从画面右下角入镜，背景是夕阳下的海平线。
- **自拍（少数）**：仅当明确晒脸/对镜/前置自拍；须写五官与外貌，但构图是**前置摄像头高度、臂展距离**，上半身+脸同框，**避免大头贴/脸占满屏**；不要第三人称别人给拍的人像。
- 每张配图 prompt 须描述**不同**画面/角度/主体；**不要**写风格词。
`.trim()

export function buildCharacterMomentImagePromptRules(isAnimeStyle: boolean): string {
  const styleNote = isAnimeStyle
    ? '二次元风格 prompt 禁止 photorealistic、realistic photo、DSLR、3d render。'
    : '写实/插画风格以用户配置为准，prompt 里勿写风格词。'
  return `${CHARACTER_MEDIA_IMAGE_DESCRIPTION_RULES}\n- ${styleNote}`
}
