export function buildCharacterForwardHistorySituationBias(params: {
  situation: ForwardHistoryEvidenceSituation | null
  userForwardedHistoryTitle?: string
}): string {
  const lines: string[] = []
  const userTitle = params.userForwardedHistoryTitle?.trim()

  if (userTitle) {
    lines.push(
      '【系统提示：用户刚向你转发了一份聊天记录卡片】',
      `- 用户甩来的标题参考：《${userTitle}》`,
      '- 若你人设会在争执/澄清时「甩记录」自证或反制，且当前语境需要，可用 `<forward_history>` **回敬一份**你与他人的对话摘录（整轮最多 1 份）；不必机械复述用户那份全文。',
      '- 若只是闲聊、或用户未质疑你，**不要**为了回发而回发，文字接话即可。',
    )
  }

  if (params.situation) {
    lines.push(
      `【系统提示：当前语境像「${SITUATION_LABEL[params.situation]}」】`,
      '- 若符合你的人设与关系，**可以**用 `<forward_history>` 发一份聊天记录卡片当证据/瓜料/凭证；整轮最多 1 份，**条数按情境写够，不设 10～12 条上限**（举证类宜写长）。',
      '- 临时 NPC 的微信昵称要**有网感**（如橘子汽水、尝一口奶糖），勿用「快递小哥阿伟」这类直白称呼；头像由客户端自动配图。',
      '- 若用一句话更自然，或你不愿暴露第三人对话，**只发文字**，不要硬凑卡片。',
    )
  }

  if (!lines.length) return ''
  return lines.join('\n')
}
