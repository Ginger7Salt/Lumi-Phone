/** 角色向用户抛出「伪造聊天记录卡片」的适用情境（用于本轮 replyBias 轻推，最终仍由模型判断） */
export type ForwardHistoryEvidenceSituation =
  | 'confrontation'
  | 'scam_proof'
  | 'gossip'
  | 'ask_help'
  | 'work_handoff'
  | 'mutual_proof'
  | 'user_explicit'

const SITUATION_RULES: Array<{ id: ForwardHistoryEvidenceSituation; patterns: RegExp[] }> = [
  {
    id: 'user_explicit',
    patterns: [
      /(?:发|给|看|晒|甩|拿).{0,12}(?:聊天记录|聊天截图|记录|证据)/u,
      /聊天记录.{0,10}(?:看看|看一下|瞧瞧|发我|给我)/u,
      /(?:你跟|你和|跟).{0,8}.+.{0,6}(?:说了什么|怎么说的|聊了什么)/u,
      /(?:证明|自证|澄清).{0,8}(?:一下|给我)/u,
    ],
  },
  {
    id: 'confrontation',
    patterns: [
      /吵架|闹矛盾|怼|对峙|扯皮|冤枉|污蔑|栽赃|甩锅|对峙/u,
      /(?:你|他|她).{0,6}(?:做错|说谎|撒谎|骗人|先动手)/u,
      /(?:证据|记录|聊天).{0,6}(?:甩|拿|拍|给)/u,
    ],
  },
  {
    id: 'scam_proof',
    patterns: [
      /被骗|被坑|诈骗|维权|举报|客服|报警|转账.{0,6}(?:骗|坑)/u,
      /(?:亲友|朋友|家人).{0,6}(?:看|帮).{0,6}(?:记录|证据)/u,
    ],
  },
  {
    id: 'gossip',
    patterns: [
      /吃瓜|八卦|瓜料|趣事|劲爆|围观|乐子|传疯了/u,
      /(?:给你看|你看这个).{0,10}(?:聊天记录|他们说|聊的)/u,
    ],
  },
  {
    id: 'ask_help',
    patterns: [
      /(?:帮我|帮忙).{0,10}(?:分析|看看|出主意|参谋|想想办法)/u,
      /(?:怎么办|咋整|怎么处理).{0,12}(?:聊天记录|记录|这事)/u,
    ],
  },
  {
    id: 'work_handoff',
    patterns: [
      /(?:工作|对接|项目|客户|甲方|乙方).{0,12}(?:记录|留证|凭证|同步|交接)/u,
      /(?:转发|同步).{0,8}(?:沟通|聊天).{0,6}(?:内容|记录)/u,
    ],
  },
  {
    id: 'mutual_proof',
    patterns: [
      /(?:互相|彼此).{0,6}(?:举证|甩记录|晒记录)/u,
      /(?:我没说谎|我可没骗|你自己看记录|记录在这)/u,
      /(?:对峙|对质|当面对)/u,
    ],
  },
]

const SITUATION_LABEL: Record<ForwardHistoryEvidenceSituation, string> = {
  user_explicit: '用户明确要求看记录/证据',
  confrontation: '吵架闹矛盾·拿记录当证据怼人',
  scam_proof: '被坑被骗·向亲友或维权方举证',
  gossip: '吃瓜八卦·分享别人的趣事瓜料',
  ask_help: '求助分析·发记录让对方出主意',
  work_handoff: '工作对接·留凭证同步沟通',
  mutual_proof: '互相举证对峙·证明自己没说谎',
}

/** 从用户最近一条文字判断：本轮是否「像微信里甩聊天记录」的情境 */
export function detectForwardHistoryEvidenceSituation(
  text: string | null | undefined,
): ForwardHistoryEvidenceSituation | null {
  const t = String(text ?? '').trim()
  if (!t) return null
  for (const rule of SITUATION_RULES) {
    if (rule.patterns.some((re) => re.test(t))) return rule.id
  }
  return null
}

