export const MBTI_LIST = ['INTJ', 'INTP', 'ENTJ', 'ENTP', 'INFJ', 'INFP', 'ENFJ', 'ENFP', 'ISTJ', 'ISFJ', 'ESTJ', 'ESFJ', 'ISTP', 'ISFP', 'ESTP', 'ESFP'] as const

export type MbtiType = (typeof MBTI_LIST)[number]

export function normalizeMbti(mbti?: string | null): MbtiType | null {
  const k = String(mbti ?? '').trim().toUpperCase()
  if (!k) return null
  return (MBTI_LIST as readonly string[]).includes(k) ? (k as MbtiType) : null
}

export function getMbtiPersonalityWorldBookName(mbti: MbtiType): string {
  return `${mbti}人格设定`
}

export function isMbtiPersonalityWorldBookName(name: string | null | undefined): name is string {
  const n = String(name ?? '').trim()
  return MBTI_LIST.some((t) => n === getMbtiPersonalityWorldBookName(t))
}

