export function sanitizeEpilogueExtensionNewContent(raw: string): string {
  let s = String(raw ?? '')
  if (!s.trim()) return s
  for (const [re, to] of EPILOGUE_EXTREME_PHRASE_REPLACEMENTS) {
    s = s.replace(re, to)
  }
  return s
}
